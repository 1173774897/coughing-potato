package entity;

public class Book {
	private String id = "";
    private String title = "";
    private String author = "";
    private String translater = "";
    private String publisher = "";
    private String isbn = "";
    private String pic = "";
    private String description = "";
    private Double doubanrating = 0.0;
    private String category = "";
    private String updateat = "";
    
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getTranslater() {
		return translater;
	}
	public void setTranslater(String translater) {
		this.translater = translater;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Double getDoubanrating() {
		return doubanrating;
	}
	public void setDoubanrating(Double doubanrating) {
		this.doubanrating = doubanrating;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getUpdateat() {
		return updateat;
	}
	public void setUpdateat(String updateat) {
		this.updateat = updateat;
	}

}  
